/**
 * Main generation function
 */
function generate() {
  const cssProperties = generateCSSProperties();
  const utilityClasses = generateUtilityClasses();

  const fullCSS = cssProperties + '\n' + utilityClasses;

  const outputPath = join(process.cwd(), 'src', 'app', 'design-system-generated.css');

  writeFileSync(outputPath, fullCSS, 'utf-8');

  console.log('✅ Design system CSS generated successfully!');
  console.log(`📁 Output: ${outputPath}`);
  console.log(`📊 Generated ${fullCSS.split('\n').length} lines of CSS`);
}

// Run if executed directly
if (require.main === module) {
  generate();
}

export { generate };
