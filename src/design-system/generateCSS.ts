/**
 * CSS Generation Script
 *
 * Converts design tokens from tokens.config.ts into CSS custom properties
 * and Tailwind CSS v4 compatible utilities.
 *
 * Usage:
 * - Run automatically during build: `pnpm build`
 * - Run manually: `ts-node src/design-system/generateCSS.ts`
 *
 * Output: src/app/design-system-generated.css
 */

import { tokens, type ColorScale } from './tokens.config';
import { writeFileSync } from 'fs';
import { join } from 'path';

/**
 * Generate CSS custom properties from color scale
 */
function generateColorProperties(name: string, scale: ColorScale): string {
  return Object.entries(scale)
    .map(([key, value]) => `  --color-${name}-${key}: ${value};`)
    .join('\n');
}

/**
 * Generate all CSS custom properties
 */
function generateCSSProperties(): string {
  const lines: string[] = [
    '/**',
    ' * Auto-generated Design System Tokens',
    ' * Generated from: src/design-system/tokens.config.ts',
    ' * Do not edit this file directly - edit tokens.config.ts instead',
    ' */',
    '',
    ':root {',
    '  /* ===== Colors ===== */',
    '',
    '  /* Primary (Sapphire) */',
    generateColorProperties('primary', tokens.colors.primary),
    '',
    '  /* Secondary (Amethyst) */',
    generateColorProperties('secondary', tokens.colors.secondary),
    '',
    '  /* Accent (Emerald) */',
    generateColorProperties('accent', tokens.colors.accent),
    '',
    '  /* Neutral (Grays) */',
    generateColorProperties('neutral', tokens.colors.neutral),
    '',
    '  /* Extended Palettes */',
    generateColorProperties('emerald', tokens.colors.emerald),
    generateColorProperties('sapphire', tokens.colors.sapphire),
    generateColorProperties('ruby', tokens.colors.ruby),
    generateColorProperties('amethyst', tokens.colors.amethyst),
    '',
    '  /* ===== Spacing ===== */',
    ...Object.entries(tokens.spacing).map(
      ([key, value]) => `  --spacing-${key}: ${value};`
    ),
    '',
    '  /* ===== Typography ===== */',
    '',
    '  /* Font Families */',
    ...Object.entries(tokens.typography.fontFamily).map(
      ([key, value]) => `  --font-${key}: ${value};`
    ),
    '',
    '  /* Font Sizes */',
    ...Object.entries(tokens.typography.fontSize).map(
      ([key, value]) => `  --text-${key}: ${value};`
    ),
    '',
    '  /* Font Weights */',
    ...Object.entries(tokens.typography.fontWeight).map(
      ([key, value]) => `  --font-weight-${key}: ${value};`
    ),
    '',
    '  /* Line Heights */',
    ...Object.entries(tokens.typography.lineHeight).map(
      ([key, value]) => `  --leading-${key}: ${value};`
    ),
    '',
    '  /* ===== Border Radius ===== */',
    ...Object.entries(tokens.borderRadius).map(([key, value]) =>
      key === 'DEFAULT'
        ? `  --rounded: ${value};`
        : `  --rounded-${key}: ${value};`
    ),
    '',
    '  /* ===== Shadows ===== */',
    ...Object.entries(tokens.shadows).map(
      ([key, value]) => `  --shadow-${key}: ${value};`
    ),
    '',
    '  /* ===== Animation ===== */',
    '',
    '  /* Easing Functions */',
    ...Object.entries(tokens.animation.easing).map(
      ([key, value]) => `  --ease-${key}: cubic-bezier(${value.join(', ')});`
    ),
    '',
    '  /* Durations */',
    ...Object.entries(tokens.animation.duration).map(
      ([key, value]) => `  --duration-${key}: ${value}ms;`
    ),
    '',
    '  /* ===== Glass Effects ===== */',
    '',
    '  /* Glass SM */',
    `  --glass-sm-bg: ${tokens.glass.sm.background};`,
    `  --glass-sm-blur: ${tokens.glass.sm.blur};`,
    `  --glass-sm-border: ${tokens.glass.sm.border};`,
    `  --glass-sm-shadow: ${tokens.glass.sm.shadow};`,
    '',
    '  /* Glass MD */',
    `  --glass-md-bg: ${tokens.glass.md.background};`,
    `  --glass-md-blur: ${tokens.glass.md.blur};`,
    `  --glass-md-border: ${tokens.glass.md.border};`,
    `  --glass-md-shadow: ${tokens.glass.md.shadow};`,
    '',
    '  /* Glass LG */',
    `  --glass-lg-bg: ${tokens.glass.lg.background};`,
    `  --glass-lg-blur: ${tokens.glass.lg.blur};`,
    `  --glass-lg-border: ${tokens.glass.lg.border};`,
    `  --glass-lg-shadow: ${tokens.glass.lg.shadow};`,
    '',
    '  /* Glass Card */',
    `  --glass-card-bg: ${tokens.glass.card.background};`,
    `  --glass-card-blur: ${tokens.glass.card.blur};`,
    `  --glass-card-border: ${tokens.glass.card.border};`,
    `  --glass-card-shadow: ${tokens.glass.card.shadow};`,
    '',
    '  /* ===== Backgrounds ===== */',
    ...Object.entries(tokens.background).map(
      ([key, value]) => `  --bg-${key}: ${value};`
    ),
    '}',
  ];

  return lines.join('\n');
}

